package lze.englishstudy;

public class EnglishWord {
	private int wordid;
	private String word,wordsound,wordmean,wordexample;
	public int getWordid() {
		return wordid;
	}
	public void setWordid(int wordid) {
		this.wordid = wordid;
	}
	public String getWord() {
		return word;
	}
	public void setWord(String word) {
		this.word = word;
	}
	public String getWordsound() {
		return wordsound;
	}
	public void setWordsound(String wordsound) {
		this.wordsound = wordsound;
	}
	public String getWordmean() {
		return wordmean;
	}
	public void setWordmean(String wordmean) {
		this.wordmean = wordmean;
	}
	public String getWordexample() {
		return wordexample;
	}
	public void setWordexample(String example) {
		this.wordexample = example;
	}
}
